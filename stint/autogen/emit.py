"""Render Change records (or empty stubs) into Python migration file source.

Output is always a complete, importable Python module that satisfies the
migration loader's contract: module-level `revision`, `down_revision`,
`async def upgrade()`, `async def downgrade()`.

Downgrade policy: autogenerated migrations always emit
``op.unsupported(...)`` in their downgrade body. Alembic-style auto-downgrade
is notoriously unreliable (updates can't be auto-inverted without before-state;
deletes can't be auto-restored). The honest move is to refuse and let the
author write the inverse explicitly when they need it.
"""

